import 'dart:core';
void main() {
  var soma = 4+7;
  print('o resultado da soma é ${soma}');

  var subtracao = 11-4;
  print('o resultado de subtração é ${subtracao}');

  var inversao = -(-5);
  print('o resultado da inversão é ${inversao}');

  var multiplicacao = 5*3;
  print('o resultado a multiplicação é${multiplicacao}');

  var divisao = 16/4;
  print('o resultado da divisão é ${divisao}');

  var divisao_resultado_inteiro = 5 ~/ 2;
  print('o resultado inteiro de divisão é ${divisao_resultado_inteiro}');

  var modulo = 9 % 2;
  print('o resto da divisão (modulo) é ${modulo}');

}